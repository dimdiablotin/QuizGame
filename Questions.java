package com.claustredimitri.quizgame;

public class Questions {
	
	private String text;
	private String response;
	
	
	public Questions (String text, String response) {
		
		this.text = text;
		this.response = response;
		
	}
	
	
	public String getText() {
		return text;
	}
	
	public String getResponse() {
		return response;
	}
	
	
	
	
	

}
